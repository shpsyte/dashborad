// eslint-disable-next-line no-unused-vars
class User {

    constructor(name, gender, birth, country, email, password, photo, admin) {
        this.name = name;
        this.gender = gender;
        this.birth = birth;
        this.country = country;
        this.email = email;
        this.password = password;
        this.photo = password;
        this.photo = photo;
        this.admin = admin;
    }
}